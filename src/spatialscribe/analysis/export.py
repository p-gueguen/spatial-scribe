def _section_annotation_quality(adata) -> str | None:
    """The section AQI + its A/C/M/G components, from ``uns['annotation_quality']`` (stamped by the
    pipeline's qc_funnel stage). The AQI is a 0-1 quality INDEX (monotone in quality, NOT P(correct));
    its purity term is trustworthy only when a reference grounds the markers, so an index_only regime
    (no >=3-method ensemble) carries the honest caveat. Returns an HTML fragment or None."""
    try:
        aq = adata.uns.get("annotation_quality")
        if not isinstance(aq, dict):
            return None
        aqi = aq.get("aqi") if isinstance(aq.get("aqi"), dict) else None
        if not aqi or aqi.get("aqi") is None:
            return None
        comp = aqi.get("components") or {}
        rows = [("AQI (0-1 section quality index, not P(correct))", f"{float(aqi['aqi']):.2f}")]
        for k, label in (("A", "Panel/depth adequacy (ceiling)"), ("C", "Contamination / purity"),
                         ("M", "Marker fidelity"), ("G", "Cross-method agreement")):
            v = comp.get(k)
            if v is not None:
                rows.append((label, f"{float(v):.2f}"))
        if aqi.get("argmin"):
            rows.append(("Limiting term", str(aqi["argmin"])))
        # Honesty note: without a >=3-method ensemble the index cannot see a coherent whole-cluster
        # mislabel (it scores only the labels present). Surface it, matching the app's AQI card.
        regime = aqi.get("regime") or (aqi.get("abstention") or {}).get("regime")
        if regime == "index_only" or comp.get("G") is None:
            rows.append(("Caveat", "no &ge;3-method ensemble voted - the index cannot see a coherent "
                                   "whole-cluster mislabel; run the reference annotators to confirm"))
        return _table_html(rows)
    except Exception:
        return None


def _section_panel_warnings(adata) -> str | None:
    try:
        pc = adata.uns.get("panel_check")
        if not isinstance(pc, dict):
            return None
        from . import rejection as _rej
        warns = _rej.panel_resolvability_warnings(adata, pc)
        if not warns:
            return None
        rows = [(w.get("cell_type", "?"), w.get("status", ""), int(w.get("n_cells", 0) or 0),
                 w.get("message", "")) for w in warns]
        return _table_html(rows, headers=("Cell type", "Status", "Cells", "Warning"))
    except Exception:
        return None


def render_analysis_report(adata, action_log: list[dict] | None = None, tissue: str = "melanoma",
                           path: str | Path = "report.html", source_path: str | None = None) -> Path:
    """Assemble a self-contained shareable HTML report of the whole run.

    Pulls the section summary, QC medians, panel-check verdict, annotatability and
    reference-match blocks from ``adata`` (whatever has been computed), inlines matplotlib figures
    (composition + spatial map) as base64 PNGs, and appends the re-runnable script. Every block is
    optional - the report shows what ran and silently omits what did not. Self-contained (no kaleido,
    no external assets)."""
    sections: list[tuple[str, str]] = []

    # 1) Section overview
    plat = adata.uns.get("platform", "?")
    npan = adata.uns.get("n_panel_genes", adata.n_vars)
    ov = [("Cells", f"{adata.n_obs:,}"), ("Genes (panel)", f"{npan:,}"),
          ("Platform", plat), ("Tissue context", tissue)]
    if "cell_type" in adata.obs:
        ov.append(("Cell types", str(adata.obs["cell_type"].astype(str).nunique())))
    sections.append(("Section", _table_html(ov)))

    # 1b) Pipeline stages - disclose which optional arms ran vs skipped/failed, so a marker-only run
    # is never read as one where SPLIT / malignant / self-heal actually fired (honest by disclosure,
    # not by omission). Populated by pipeline.run_pipeline; absent for an interactive ad-hoc session.
    pl = adata.uns.get("pipeline")
    if isinstance(pl, dict) and isinstance(pl.get("stages"), list) and pl["stages"]:
        srows = []
        for stg in pl["stages"]:
            kind, _, why = str(stg.get("status", "")).partition(":")
            label = {"ok": "ran", "skipped": "skipped", "failed": "FAILED"}.get(kind, kind or "?")
            srows.append((stg.get("name", "?"), label + (f" - {why}" if why else "")))
        sections.append(("Pipeline stages", _table_html(srows, headers=("Stage", "Status"))))

    # 2) QC medians
    try:
        from . import qc as _qc
        if "total_counts" in adata.obs:
            s = _qc.qc_summary(adata)
            sections.append(("Quality control", _table_html(
                [(k, (f"{v:.3g}" if isinstance(v, float) else v)) for k, v in s.items()])))
    except Exception:
        pass

    # 3) Panel check
    pc = adata.uns.get("panel_check")
    if isinstance(pc, dict) and isinstance(pc.get("coverage"), dict):
        cov = pc["coverage"]
        greens = [ct for ct, d in cov.items() if isinstance(d, dict) and d.get("status") == "green"]
        reds = [ct for ct, d in cov.items() if isinstance(d, dict) and d.get("status") == "red"]
        sections.append(("Panel adequacy", _table_html(
            [("Resolvable", ", ".join(greens) or "none"),
             ("Cannot resolve", ", ".join(reds) or "none")])))
        frag = _section_panel_warnings(adata)
        if frag:
            sections.append(("Panel resolvability warnings", frag))

    # 4) Annotatability / confidence
    if "annotation_verdict" in adata.obs:
        v = adata.obs["annotation_verdict"].astype(str)
        rows = [("PASS", f"{(v == 'PASS').mean():.0%}"), ("WARN", f"{(v == 'WARN').mean():.0%}"),
                ("Abstained", f"{(v == 'FAIL').mean():.0%}")]
        # Say what the abstention RESTS on, so a reader does not over-trust the greying. Measured:
        # the confidence heuristic barely ranks correct cells (within-lineage AUC ~0.54); cross-method
        # agreement does (~0.77). apply_confidence writes uns['abstention_basis'] with that call.
        ab = adata.uns.get("abstention_basis")
        if isinstance(ab, dict):
            rows.append(("Abstention basis",
                         "cross-method ensemble (reliable)" if ab.get("trustworthy")
                         else "confidence heuristic only (advisory - run the reference ensemble to make it reliable)"))
        sections.append(("Annotation confidence", _table_html(rows)))

    # 6) Reference match
    rm = adata.uns.get("reference_match")
    if isinstance(rm, dict) and rm.get("global"):
        g = rm["global"]
        sections.append(("Reference &harr; panel match", _table_html(
            [("Panel-gene overlap", f"{g.get('gene_overlap_frac', 0):.0%}"),
             ("Mean per-type F1", g.get("mean_f1")), ("Verdict", g.get("verdict"))])))

    # 6b) Extra grounded sections - each guarded, appended only when its field(s) are present.
    for heading, builder in (("Annotation quality (AQI)", _section_annotation_quality),
                             ("QC funnel flags", _section_qc_flags),
                             ("Cell states", _section_cell_states),
                             ("Spatial niches", _section_niches),
                             ("De-novo programs", _section_programs),
                             ("Malignant calling", _section_malignant),
                             ("Untyped-cell reasons", _section_rejection)):
        frag = builder(adata)
        if frag:
            sections.append((heading, frag))

    # 7) Figures (inline 150-DPI base64 for a light HTML)
    for name, fig in report_figures(adata):
        src = fig_to_base64(fig, dpi=150)
        if src:
            sections.append((name.replace("_", " ").title(), f'<img src="{src}" alt="{name}">'))
        import matplotlib.pyplot as _plt
        _plt.close(fig)

    # 8) Re-runnable script
    script = build_runnable_script(action_log, adata=adata, tissue=tissue, source_path=source_path)
    esc = script.replace("&", "&amp;").replace("<", "&lt;").replace(">", "&gt;")
    sections.append(("Re-runnable analysis", f"<pre>{esc}</pre>"))

    sections.append(("", "<p class='muted'>Generated by SpatialScribe. Self-contained; no server "
                     "needed - open in any browser.</p>"))
    return render_report(f"SpatialScribe report - {tissue}", sections, path)


