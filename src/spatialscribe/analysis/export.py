def _section_programs(adata) -> str | None:
    try:
        if not ("program" in adata.obs and "program_loadings" in adata.varm
                and "program_scores" in adata.obsm):
            return None
        import numpy as np
        from . import programs as _p
        load = np.asarray(adata.varm["program_loadings"])
        sc = np.asarray(adata.obsm["program_scores"])
        var = np.asarray(adata.var_names)
        dom = np.asarray(adata.obsm["programs"]).argmax(1) if "programs" in adata.obsm else None
        rows = []
        for k in range(load.shape[1]):
            top = ", ".join(str(g) for g in var[np.argsort(-load[:, k])[:6]])
            in_mask = (dom == k) if dom is not None else np.zeros(sc.shape[0], dtype=bool)
            pin = f"{sc[in_mask, k].mean():.2f}" if in_mask.any() else "n/a"
            pout = f"{sc[~in_mask, k].mean():.2f}" if (~in_mask).any() else "n/a"
            spec = _p._auroc(sc[:, k], in_mask)
            rows.append((f"Program {k}", int(in_mask.sum()), top, pin, pout,
                         f"{spec:.3g}" if spec == spec else "n/a"))   # spec==spec drops NaN
        return _table_html(rows, headers=("Program", "Cells", "Top genes", "signature (own)",
                                          "signature (rest)", "specificity (AUROC)"))
    except Exception:
        return None


def _section_malignant(adata) -> str | None:
    try:
        if "malignant_score" not in adata.obs:
            return None
        import numpy as np
        rows = [("Marker score > 0.6", _pct(np.asarray(adata.obs["malignant_score"], dtype=float) > 0.6))]
        if "is_malignant" in adata.obs:
            rows.append(("InSituCNV malignant", _pct(adata.obs["is_malignant"].to_numpy(dtype=bool))))
            if "cnv_score" in adata.obs:
                rows.append(("Median CNV score", f"{np.nanmedian(np.asarray(adata.obs['cnv_score'], dtype=float)):.3g}"))
        if "cancerfinder_malignant" in adata.obs:
            rows.append(("Cancer-Finder malignant", _pct(adata.obs["cancerfinder_malignant"].to_numpy(dtype=bool))))
        if "is_malignant" in adata.obs and "cancerfinder_malignant" in adata.obs:
            cfp = np.asarray(adata.obs.get("cancerfinder_prob", np.full(adata.n_obs, np.nan)), dtype=float)
            cnvs = np.asarray(adata.obs.get("cnv_score", np.full(adata.n_obs, np.nan)), dtype=float)
            m = ~np.isnan(cfp) & ~np.isnan(cnvs)
            if m.any():
                conc = (adata.obs["cancerfinder_malignant"].to_numpy(dtype=bool)[m]
                        == adata.obs["is_malignant"].to_numpy(dtype=bool)[m]).mean()
                rows.append(("Learned-caller concordance", f"{100 * conc:.0f}% (over {int(m.sum()):,} cells)"))
        return _table_html(rows, headers=("Caller", "Value"))
    except Exception:
        return None


