def _cat_palette(n: int):
    import matplotlib.cm as cm
    base = list(cm.tab20.colors) + list(cm.tab20b.colors) + list(cm.tab20c.colors)
    return [base[i % len(base)] for i in range(max(1, n))]


def composition_table(adata, key: str | None = None):
    """``(counts_per_cell_type, abstained_fraction)`` for the section.

    Abstained cells are EXCLUDED from the counts (they are not a cell type) while their share is
    returned so the caller can state it. Dropping them silently would change the denominator behind
    the reader's back; drawing them as bars would assert a lineage that was explicitly not called.

    ``abstained_fraction`` counts every cell whose FINAL label is not a lineage, which is a superset
    of ``apply_confidence``'s ``pct_abstain`` (the confidence-verdict FAIL rate): it also covers the
    honest ``Novel / unknown`` calls and the leiden-NaN ``Unassigned`` cells, which are not lineages
    even when their confidence is fine. Measured on a real Prime 5K section: 15.5% here vs 10.8%
    pct_abstain. The two numbers answer different questions and are both correct.
    """
    from . import annotate as _an

    key = key or _an.annotation_key(adata)
    lab = adata.obs[key].astype(str)
    typed = ~_an.abstention_mask(lab.to_numpy())
    abstained_frac = float((~typed).mean()) if len(lab) else 0.0
    return lab[typed].value_counts(), abstained_frac


def _fig_composition(adata, key: str = "cell_type"):
    """Horizontal bar chart of cell-type composition (matplotlib). Returns a Figure or None."""
    import matplotlib
    matplotlib.use("Agg")
    import matplotlib.pyplot as plt
    if key not in adata.obs:
        return None
    counts, abstained = composition_table(adata, key)
    if counts.empty:
        return None
    vc = counts.head(20)[::-1]
    fig, ax = plt.subplots(figsize=(6.5, max(2.2, 0.32 * len(vc))))
    ax.barh(range(len(vc)), vc.to_numpy(), color=_cat_palette(len(vc)))
    ax.set_yticks(range(len(vc)))
    ax.set_yticklabels([str(i) for i in vc.index], fontsize=8)
    ax.set_xlabel("cells")
    ax.set_title(f"Composition ({key}, {int(vc.sum()):,} typed cells; "
                 f"{abstained:.1%} abstained, excluded)")
    fig.tight_layout()
    return fig


def _fig_spatial(adata, key: str = "cell_type", max_points: int = 60000):
    """Spatial scatter colored by a categorical obs field (matplotlib). Returns a Figure or None."""
    import matplotlib
    matplotlib.use("Agg")
    import matplotlib.pyplot as plt
    import numpy as np
    if "spatial" not in adata.obsm or key not in adata.obs:
        return None
    from . import annotate as _an

    xy = np.asarray(adata.obsm["spatial"], dtype=float)
    # Abstained cells stay on the map (they occupy real tissue) but collapse into ONE grey category:
    # five abstention pseudo-labels became five coloured legend entries that read as lineages.
    lab = _an.collapse_abstention(adata.obs[key]).astype(str).to_numpy()
    n = xy.shape[0]
    if n > max_points:                                        # downsample dense sections for the PNG
        idx = np.random.default_rng(0).choice(n, size=max_points, replace=False)
        xy, lab = xy[idx], lab[idx]
    typed_cats = sorted(c for c in set(lab) if c != _an.NOT_ASSIGNED)
    colors = dict(zip(typed_cats, _cat_palette(len(typed_cats))))
    cats = typed_cats
    if _an.NOT_ASSIGNED in set(lab):                          # drawn first, under the typed cells
        colors[_an.NOT_ASSIGNED] = (0.78, 0.78, 0.78)
        cats = [_an.NOT_ASSIGNED, *typed_cats]
    fig, ax = plt.subplots(figsize=(7, 6.4))
    for c in cats:
        m = lab == c
        ax.scatter(xy[m, 0], xy[m, 1], s=2, c=[colors[c]], label=c, linewidths=0, rasterized=True)
    ax.set_aspect("equal")
    ax.invert_yaxis()
    ax.axis("off")
    ax.set_title(f"Spatial map ({key})")
    if len(cats) <= 24:
        ax.legend(markerscale=3, fontsize=6, loc="center left", bbox_to_anchor=(1, 0.5), frameon=False)
    fig.tight_layout()
    return fig


def _table_html(rows: list[tuple], headers: tuple[str, ...] | None = None) -> str:
    """Small HTML table from a list of tuples."""
    def esc(v):
        return str(v).replace("&", "&amp;").replace("<", "&lt;").replace(">", "&gt;")
    head = f"<tr>{''.join(f'<th>{esc(h)}</th>' for h in headers)}</tr>" if headers else ""
    body = "".join("<tr>" + "".join(f"<td>{esc(c)}</td>" for c in r) + "</tr>" for r in rows)
    return f"<table>{head}{body}</table>"


