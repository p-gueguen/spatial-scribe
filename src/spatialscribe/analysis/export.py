"""Exports - annotated .h5ad, a re-runnable script, and a self-contained HTML report.

These are the "outlasts the week" deliverables: the scientist hands the `.h5ad` to a
bioinformatician, shares the HTML with the PI, and re-runs the exact analysis from the
script (assembled from the session action log).
"""

from __future__ import annotations

import base64
import io as _io
from pathlib import Path

_SCRIPT_HEADER = '''# Auto-generated by SpatialScribe - reproduces this section's analysis end to end.
# Requires the SpatialScribe environment (`pixi install -e main`).
from spatialscribe.analysis import io, capabilities as cap

ctx = cap.RunContext(tissue="{tissue}")
'''

_LOADER_PLACEHOLDER = (
    '\n# 1) Point this at your section (a Xenium/CosMx/MERSCOPE output folder or a .h5ad):\n'
    'adata = io.load("PATH/TO/YOUR/SECTION").adata\n\n# 2) Reproduce the analysis:\n'
)


def _loader(source_path: str | None) -> str:
    """The section-loading block: the real path when known (directly runnable), else a placeholder."""
    if source_path:
        return (f'\n# 1) Your section:\nadata = io.load({source_path!r}).adata\n\n'
                '# 2) Reproduce the analysis:\n')
    return _LOADER_PLACEHOLDER

# Control probes are kept through QC (so pct_control is measured on the real matrix), then dropped
# before clustering/annotation. Emitted right after compute_qc.
_CONTROL_DROP = (
    '# Controls were measured in QC above; drop them for clustering/annotation:\n'
    'if "control" in adata.var.columns:\n'
    '    adata = adata[:, ~adata.var["control"].to_numpy(dtype=bool)].copy()\n'
)

# The canonical SpatialScribe pipeline, in order, with the params the demo cache was built with
# and a predicate that says "this step's primary output is present in the current object". The
# re-runnable script emits every step whose output is present (so it reproduces the SHOWN state,
# not just whatever was clicked this session), overriding params from the session log when a step
# was run interactively.
_PIPELINE = [
    ("compute_qc",  {},                  lambda a: "total_counts" in a.obs),
    ("panel_check", {},                  lambda a: "panel_check" in a.uns),
    ("cluster",     {"resolution": 1.0}, lambda a: "leiden" in a.obs),
    ("annotate",    {},                  lambda a: "cell_type" in a.obs),
    ("niches",      {},                  lambda a: "niche" in a.obs),
]


def _h5ad_safe(value):
    """Recursively replace '/' in dict keys - h5py forbids slashes in group keys, so a nested uns
    dict keyed by cell types like 'Epithelial/Tumor' or 'Stromal/CAF' (e.g. panel_check.coverage)
    otherwise makes write_h5ad raise. Values and non-dict containers pass through unchanged."""
    if isinstance(value, dict):
        return {str(k).replace("/", "_"): _h5ad_safe(v) for k, v in value.items()}
    if isinstance(value, list):
        return [_h5ad_safe(v) for v in value]
    if isinstance(value, tuple):
        return tuple(_h5ad_safe(v) for v in value)
    return value


def export_h5ad(adata, path: str | Path) -> Path:
    """Write the annotated AnnData (cell types, states, QC, panel_check in .uns).

    Sanitizes only ``uns`` keys (cheap - no cell-matrix copy) so a section that has run Panel check
    still exports; the live object's ``uns`` is restored afterwards so the running session is intact.
    """
    path = Path(path)
    path.parent.mkdir(parents=True, exist_ok=True)
    orig_uns = adata.uns
    try:
        adata.uns = _h5ad_safe(dict(orig_uns))
        adata.write_h5ad(path)
    finally:
        adata.uns = orig_uns
    return path


def _render_entry(entry: dict) -> str:
    """Render one action-log record to a code line.

    Structured records ``{name, params}`` (the current provenance format, emitted by both
    the wizard and the copilot) render to a ``cap.run(...)`` call assembled from the
    registry, so the script never drifts from the real function signature. Legacy
    ``{label, code}`` records fall back to their stored snippet.
    """
    if "name" in entry and "code" not in entry:
        import json
        name = entry["name"]
        # Normalize params to JSON-safe primitives so a numpy/other scalar degrades to a plain
        # literal (the generated script imports neither numpy nor anndata).
        params = json.loads(json.dumps(entry.get("params", {}), default=str))
        return f"# --- {name} ---\ncap.run(adata, {name!r}, {params!r}, ctx)\n"
    code = entry.get("code", "")
    if code:
        return f"# --- {entry.get('label', '')} ---\n{code}\n"
    return ""


def build_runnable_script(action_log: list[dict] | None, adata=None, tissue: str = "melanoma",
                          source_path: str | None = None) -> str:
    """A complete, runnable reproduction script: header + section loader + the pipeline.

    Emits the canonical SpatialScribe pipeline (:data:`_PIPELINE`) in order. A step is included
    when its primary output is present on ``adata`` (so the script reproduces the SHOWN state,
    not merely the steps clicked this session) or when it appears in the session log; params come
    from the log when the step was run interactively, else the canonical demo params. Meaningful
    copilot-run analyses outside the standard pipeline (those that produce state) are appended in
    log order. With ``adata=None`` every canonical step is emitted (no state to introspect).
    """
    logged: dict[str, dict] = {}
    for entry in (action_log or []):
        if isinstance(entry, dict) and entry.get("name"):
            logged[entry["name"]] = entry.get("params") or {}

    parts = [_SCRIPT_HEADER.format(tissue=tissue), _loader(source_path)]
    emitted: list[str] = []
    for name, default_params, present in _PIPELINE:
        include = (name in logged) or (present(adata) if adata is not None else True)
        if not include:
            continue
        parts.append(_render_entry({"name": name, "params": logged.get(name, default_params)}))
        if name == "compute_qc":
            parts.append(_CONTROL_DROP)
        emitted.append(name)

    # Copilot-run analyses beyond the standard pipeline that actually produce state (e.g. subcluster,
    # malignant_score, discover_programs), plus any LEGACY {label, code} records - appended once, in
    # log order. Legacy snippets are emitted verbatim (provenance) so a hand-logged step is never lost.
    try:
        from . import capabilities as _cap
        pipeline_names = {p[0] for p in _PIPELINE}
        for entry in (action_log or []):
            if not isinstance(entry, dict):
                continue
            name = entry.get("name")
            if name:
                if name in emitted or name in pipeline_names:
                    continue
                cap = _cap.REGISTRY.get(name)
                if cap is not None and cap.produces:
                    parts.append(_render_entry({"name": name, "params": entry.get("params") or {}}))
                    emitted.append(name)
            elif entry.get("code"):
                parts.append(_render_entry(entry))          # legacy {label, code} snippet
                emitted.append(entry.get("label") or "code")
    except Exception:
        pass

    if not emitted:
        parts.append("# (no analysis steps have run on this section yet)\n")
    return "\n".join(parts)


def export_script(action_log: list[dict], path: str | Path, tissue: str = "melanoma", adata=None,
                  source_path: str | None = None) -> Path:
    """Write the runnable reproduction script (see :func:`build_runnable_script`) to ``path``.

    ``source_path`` (the loaded section's path) is threaded into the script's loader so the emitted
    ``rerun.py`` re-loads the REAL section rather than a ``PATH/TO/YOUR/SECTION`` placeholder the
    user has to edit before it runs."""
    path = Path(path)
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(build_runnable_script(action_log, adata=adata, tissue=tissue, source_path=source_path))
    return path


def fig_to_base64(fig, dpi: int = 150) -> str:
    """Render a figure to an inline base64 ``<img>`` src (self-contained HTML).

    Handles a matplotlib ``Figure`` (``savefig``) and, when kaleido is installed, a Plotly figure
    (``to_image``). Returns an empty string if the figure cannot be rasterized (e.g. a Plotly figure
    with no kaleido), so a report never crashes on one un-exportable panel. ``dpi`` applies to the
    matplotlib path (Plotly has no dpi; it is exported at scale 2).
    """
    if hasattr(fig, "savefig"):                                   # matplotlib
        buf = _io.BytesIO()
        fig.savefig(buf, format="png", dpi=dpi, bbox_inches="tight")
        buf.seek(0)
        return "data:image/png;base64," + base64.b64encode(buf.read()).decode()
    try:                                                          # Plotly (needs kaleido)
        png = fig.to_image(format="png", scale=2)
        return "data:image/png;base64," + base64.b64encode(png).decode()
    except Exception:
        return ""


def save_figure_png(fig, path: str | Path, dpi: int = 300) -> Path | None:
    """Write a single 300-DPI PNG for a matplotlib figure (a publication-grade deliverable).

    Returns the path, or ``None`` for a non-matplotlib figure with no kaleido (never raises)."""
    path = Path(path)
    path.parent.mkdir(parents=True, exist_ok=True)
    if hasattr(fig, "savefig"):
        fig.savefig(path, format="png", dpi=dpi, bbox_inches="tight")
        return path
    try:
        fig.write_image(str(path), scale=3)                      # ~300 DPI for a Plotly fig w/ kaleido
        return path
    except Exception:
        return None


