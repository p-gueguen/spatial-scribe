def render_report(title: str, sections: list[tuple[str, str]], path: str | Path) -> Path:
    """Write a self-contained HTML report from ``(heading, html_fragment)`` sections."""
    path = Path(path)
    path.parent.mkdir(parents=True, exist_ok=True)
    body = "\n".join(f"<h2>{h}</h2>\n{frag}" for h, frag in sections)
    html = (
        f"<!doctype html><html><head><meta charset='utf-8'><title>{title}</title>"
        f"<style>{_REPORT_CSS}</style></head><body><h1>{title}</h1>{body}</body></html>"
    )
    path.write_text(html)
    return path


