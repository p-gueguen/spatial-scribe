def _pct(mask) -> str:
    import numpy as np
    m = np.asarray(mask, dtype=bool)
    return f"{100 * m.mean():.0f}%" if m.size else "0%"


def _section_cell_states(adata) -> str | None:
    try:
        if "cell_state" not in adata.obs:
            return None
        vc = adata.obs["cell_state"].astype(str).value_counts()
        n = max(1, int(adata.n_obs))
        rows = [((s if s.lower() not in ("none", "nan") else "no dominant program"),
                 int(c), f"{100 * c / n:.0f}%") for s, c in vc.items()]
        html = _table_html(rows, headers=("State", "Cells", "% of cells"))
        cols = adata.uns.get("state_columns")
        if isinstance(cols, dict) and cols:
            html += (f"<p class='muted'>Programs scored: {', '.join(map(str, cols))}.</p>")
        return html
    except Exception:
        return None


