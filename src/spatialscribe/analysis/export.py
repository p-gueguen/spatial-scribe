def _r_style(fig, *axes):
    """Apply the dark report theme to a figure and its axes in place (spines, ticks, labels)."""
    fig.patch.set_facecolor(_R_BG)
    for ax in axes:
        ax.set_facecolor(_R_BG)
        for s in ax.spines.values():
            s.set_color(_R_LINE)
        ax.tick_params(colors=_R_MUTED, labelsize=8, length=3)
        ax.xaxis.label.set_color(_R_MUTED)
        ax.yaxis.label.set_color(_R_MUTED)
        ax.title.set_color(_R_INK)


def fig_qc_histograms(adata):
    """Counts-per-cell (+ genes-per-cell) histograms on a log x-axis, with the panel-indexed count
    floor drawn as a vertical rule and the removed fraction annotated. Needs ``obs['total_counts']``;
    the genes panel is added only when ``obs['n_genes_by_counts']`` is present. Returns Figure|None."""
    import matplotlib
    matplotlib.use("Agg")
    import matplotlib.pyplot as plt
    import numpy as np
    from . import qc as _qc
    if "total_counts" not in adata.obs:
        return None
    counts = np.asarray(adata.obs["total_counts"], dtype=float)
    genes = (np.asarray(adata.obs["n_genes_by_counts"], dtype=float)
             if "n_genes_by_counts" in adata.obs else None)
    floor = float(_qc.suggest_count_floor(adata).get("floor", 0.0))
    removed = float(np.mean(counts < floor)) if floor > 0 and counts.size else 0.0

    ncol = 2 if genes is not None else 1
    fig, axes = plt.subplots(1, ncol, figsize=(4.6 * ncol, 3.2))
    axes = np.atleast_1d(axes)
    _r_style(fig, *axes)

    def _loghist(ax, vals, colour, xlabel, rule=None, rule_note=None):
        pos = vals[vals > 0]
        if pos.size == 0:
            ax.text(0.5, 0.5, "no positive values", ha="center", va="center",
                    color=_R_MUTED, transform=ax.transAxes)
            return
        bins = np.logspace(np.log10(pos.min()), np.log10(pos.max()), 44)
        ax.hist(pos, bins=bins, color=colour, alpha=0.85, edgecolor=_R_BG, linewidth=0.3)
        ax.set_xscale("log")
        ax.set_xlabel(xlabel)
        ax.set_ylabel("cells")
        if rule and rule > 0:
            ax.axvline(rule, color=_R_FAIL, linestyle="--", linewidth=1.4)
            if rule_note:
                ax.text(0.97, 0.94, rule_note, transform=ax.transAxes, ha="right", va="top",
                        color=_R_FAIL, fontsize=8.5)

    _loghist(axes[0], counts, _R_VIOLET, "transcripts / cell (log)", floor,
             f"count floor {floor:g}\n{removed * 100:.1f}% below")
    axes[0].set_title("counts per cell", fontsize=10, loc="left")
    if genes is not None:
        _loghist(axes[1], genes, "#6c8cff", "genes / cell (log)")
        axes[1].set_title("genes per cell", fontsize=10, loc="left")
    fig.tight_layout()
    return fig


def fig_qc_funnel(adata):
    """Horizontal QC funnel: cells surviving each successive gate (cumulative AND of the gates whose
    obs column is present). Gates, in pipeline order: loaded -> segmentation OK -> above count floor
    -> pure (PMP/CRISP) -> confidently/tentatively labelled. Reads only persisted columns (never
    mutates, never recomputes a heavy layer). Returns Figure|None (None if nothing beyond 'loaded')."""
    import matplotlib
    matplotlib.use("Agg")
    import matplotlib.pyplot as plt
    import numpy as np
    from . import qc as _qc
    n = int(adata.n_obs)
    if n == 0:
        return None
    keep = np.ones(n, dtype=bool)
    rows = [("loaded", n)]
    if "seg_area_flag" in adata.obs:
        keep &= adata.obs["seg_area_flag"].astype(str).to_numpy() == "ok"
        rows.append(("segmentation OK", int(keep.sum())))
    if "total_counts" in adata.obs:
        floor = float(_qc.suggest_count_floor(adata).get("floor", 0.0))
        keep &= np.asarray(adata.obs["total_counts"], dtype=float) >= floor
        rows.append(("above count floor", int(keep.sum())))
    if "crisp_impure" in adata.obs:
        keep &= ~adata.obs["crisp_impure"].to_numpy(dtype=bool)
        rows.append(("pure (PMP)", int(keep.sum())))
    if "annotation_verdict" in adata.obs:
        keep &= adata.obs["annotation_verdict"].astype(str).to_numpy() != "FAIL"
        rows.append(("confidently labelled", int(keep.sum())))
    if len(rows) < 2:                                    # only 'loaded' is computable - nothing to show
        return None

    labels = [r[0] for r in rows]
    vals = [r[1] for r in rows]
    y = np.arange(len(rows))[::-1]                       # first gate on top
    fig, ax = plt.subplots(figsize=(7.6, 0.62 * len(rows) + 1.0))
    _r_style(fig, ax)
    ax.barh(y, [n] * len(rows), color=_R_PANEL, height=0.66)          # full-width reference track
    ax.barh(y, vals, color=_R_VIOLET, height=0.66)                    # survivors
    for yi, lab, v in zip(y, labels, vals):
        ax.text(n * 0.012, yi, lab, va="center", ha="left", color=_R_BG, fontsize=9, fontweight="bold")
        ax.text(n * 0.99, yi, f"{v:,}  ({100 * v / n:.0f}%)", va="center", ha="right",
                color=_R_INK, fontsize=8.5)
    ax.set_yticks([])
    ax.set_xlim(0, n)
    ax.set_xlabel("cells surviving (cumulative)")
    ax.spines["left"].set_visible(False)
    ax.spines["top"].set_visible(False)
    ax.spines["right"].set_visible(False)
    fig.tight_layout()
    return fig


def fig_composition_bar(adata, colour_map=None, key="cell_type", top=14):
    """A single 100%-stacked horizontal composition bar of the section's cell types (rank-ordered,
    the tail merged into 'other'). ``colour_map`` maps a label to an RGB float tuple so the segments
    match the spatial map's palette; unknown labels fall back to grey. Needs ``obs[key]``. Fig|None."""
    import matplotlib
    matplotlib.use("Agg")
    import matplotlib.pyplot as plt
    import numpy as np
    if key not in adata.obs:
        return None
    vc = adata.obs[key].astype(str).value_counts()
    total = int(vc.sum())
    if total == 0:
        return None
    head = vc.head(top)
    tail = int(vc.iloc[top:].sum())
    names = list(head.index) + (["other"] if tail > 0 else [])
    counts = list(head.to_numpy()) + ([tail] if tail > 0 else [])
    cmap = colour_map or {}

    def _col(name):
        c = cmap.get(name)
        return (tuple(c) if c is not None else (0.42, 0.45, 0.5))

    fig, ax = plt.subplots(figsize=(7.6, 1.9))
    _r_style(fig, ax)
    left = 0.0
    for name, c in zip(names, counts):
        frac = 100 * c / total
        ax.barh(0, frac, left=left, color=_col(name), height=0.62, edgecolor=_R_BG, linewidth=0.6)
        if frac >= 6:                                    # only label segments wide enough to read
            ax.text(left + frac / 2, 0, f"{name}\n{frac:.0f}%", va="center", ha="center",
                    color="#0b0c10", fontsize=7.6, fontweight="bold")
        left += frac
    ax.set_xlim(0, 100)
    ax.set_ylim(-0.5, 0.5)
    ax.set_yticks([])
    ax.set_xlabel("% of cells")
    for side in ("left", "top", "right"):
        ax.spines[side].set_visible(False)
    fig.tight_layout()
    return fig


def fig_marker_dotplot(adata, key: str = "cell_type", n_types: int = 22, genes_per_type: int = 2):
    """Dot plot of the top DATA-DRIVEN marker genes per annotated cell type - the honest
    "do the labels express distinct genes?" check.

    Markers are the section's own DEGs (``rank_genes_groups``), NOT a curated dictionary, so it works
    for ANY label vocabulary - curated lineages OR a reference-transfer atlas (the fine MOCA embryo
    types osteoblast/myotube/... which no human-tissue marker dict anticipates). Dot SIZE = fraction
    of the type's cells expressing the gene; COLOUR = per-gene min-max-scaled mean expression across
    the shown types, so each column's brightest dot is the type it marks. Groups on the largest
    ``n_types`` real types (abstention labels dropped). Returns Figure|None.
    """
    import matplotlib
    matplotlib.use("Agg")
    import matplotlib.pyplot as plt
    import numpy as np
    import scipy.sparse as sp
    from collections import Counter
    from . import annotate as _an
    if key not in adata.obs:
        return None
    labels_full = adata.obs[key].astype(str).to_numpy()
    typed = ~_an.abstention_mask(labels_full)
    if typed.sum() < 20:
        return None
    types = [t for t, _ in Counter(labels_full[typed]).most_common(n_types)]
    if len(types) < 2:
        return None
    # rank genes per type on a (subsampled) view restricted to the shown types - cheap on 466 genes.
    import scanpy as sc
    keep = typed & np.isin(labels_full, types)
    sub = adata[keep].copy()
    if sub.n_obs > 60000:
        idx = np.random.default_rng(0).choice(sub.n_obs, 60000, replace=False)
        sub = sub[np.sort(idx)].copy()
    sub.obs["_k"] = sub.obs[key].astype(str).astype("category")
    if sub.obs["_k"].nunique() < 2:
        return None
    try:
        sc.tl.rank_genes_groups(sub, "_k", method="t-test", n_genes=genes_per_type + 4)
    except Exception:
        return None
    names = sub.uns["rank_genes_groups"]["names"]
    genes: list[str] = []
    for t in types:                                          # keep the type order for a readable diagonal
        if t in names.dtype.names:
            for g in list(names[t])[:genes_per_type]:
                g = str(g)
                if g in sub.var_names and g not in genes:
                    genes.append(g)
    if len(genes) < 2:
        return None
    X = sub[:, genes].X
    X = X.toarray() if sp.issparse(X) else np.asarray(X)
    lab = sub.obs[key].astype(str).to_numpy()
    frac = np.zeros((len(types), len(genes)))
    mean = np.zeros_like(frac)
    for i, t in enumerate(types):
        m = lab == t
        if m.any():
            xt = X[m]
            frac[i] = (xt > 0).mean(0)
            mean[i] = xt.mean(0)
    rng = np.clip(mean.max(0, keepdims=True) - mean.min(0, keepdims=True), 1e-9, None)
    scaled = (mean - mean.min(0, keepdims=True)) / rng

    fig, ax = plt.subplots(figsize=(max(6.0, 0.34 * len(genes) + 2.4), max(3.6, 0.30 * len(types) + 1.2)))
    _r_style(fig, ax)
    yy, xx = np.mgrid[0:len(types), 0:len(genes)]
    sca = ax.scatter(xx.ravel(), yy.ravel(), s=(12 + 150 * frac).ravel(), c=scaled.ravel(),
                     cmap="magma", edgecolors="none", vmin=0, vmax=1)
    ax.set_xticks(range(len(genes)))
    ax.set_xticklabels(genes, rotation=90, fontsize=6, color=_R_INK)
    ax.set_yticks(range(len(types)))
    ax.set_yticklabels(types, fontsize=6.5, color=_R_INK)
    ax.set_xlim(-0.5, len(genes) - 0.5)
    ax.set_ylim(-0.5, len(types) - 0.5)
    ax.invert_yaxis()
    ax.set_title("Top marker genes per cell type (data-driven DEGs)", fontsize=9)
    for s in ("top", "right"):
        ax.spines[s].set_visible(False)
    cb = fig.colorbar(sca, ax=ax, fraction=0.022, pad=0.015)
    cb.set_label("scaled mean expr", color=_R_MUTED, fontsize=6.5)
    cb.ax.tick_params(colors=_R_MUTED, labelsize=6)
    fig.tight_layout()
    return fig


