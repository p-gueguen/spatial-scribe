def fig_confidence_ridges(adata, colour_map=None, conf_key="annotation_confidence",
                          label_key="cell_type", top=12):
    """Per-cell-type confidence distributions as horizontal violins (a small-multiple ridgeline):
    one row per cell type (top-N by count), the violin drawn over ``obs[conf_key]`` in [0,1], tinted
    to match the map palette. Needs ``obs[conf_key]`` and ``obs[label_key]``. Returns Figure|None."""
    import matplotlib
    matplotlib.use("Agg")
    import matplotlib.pyplot as plt
    import numpy as np
    if conf_key not in adata.obs or label_key not in adata.obs:
        return None
    conf = np.asarray(adata.obs[conf_key], dtype=float)
    lab = adata.obs[label_key].astype(str).to_numpy()
    order = [str(k) for k in adata.obs[label_key].astype(str).value_counts().head(top).index]
    data, names, colours = [], [], []
    cmap = colour_map or {}
    for name in order:
        vals = conf[(lab == name) & np.isfinite(conf)]
        if vals.size < 10:                               # too few cells for a meaningful violin
            continue
        data.append(vals)
        names.append(name)
        c = cmap.get(name)
        colours.append(tuple(c) if c is not None else (0.66, 0.59, 0.95))
    if not data:
        return None
    pos = np.arange(len(data))
    fig, ax = plt.subplots(figsize=(7.6, 0.42 * len(data) + 1.1))
    _r_style(fig, ax)
    parts = ax.violinplot(data, positions=pos, vert=False, widths=0.9,
                          showmeans=False, showextrema=False, showmedians=True)
    for body, colour in zip(parts["bodies"], colours):
        body.set_facecolor(colour)
        body.set_edgecolor(colour)
        body.set_alpha(0.72)
    if "cmedians" in parts:
        parts["cmedians"].set_color(_R_INK)
        parts["cmedians"].set_linewidth(1.0)
    ax.set_yticks(pos)
    ax.set_yticklabels(names, fontsize=8, color=_R_INK)
    ax.set_xlim(0, 1)
    ax.set_xlabel("per-cell annotation confidence")
    for side in ("top", "right"):
        ax.spines[side].set_visible(False)
    fig.tight_layout()
    return fig


def fig_rejection_bar(adata):
    """Horizontal bar chart of why cells were left unlabelled: the reason breakdown among abstained
    cells. Prefers the granular ``obs['rejection_reason']``, else the coarse ``obs['annotation_reason']``;
    restricted to FAIL cells when ``annotation_verdict`` is present. Returns Figure|None."""
    import matplotlib
    matplotlib.use("Agg")
    import matplotlib.pyplot as plt
    import numpy as np
    import pandas as pd
    col = "rejection_reason" if "rejection_reason" in adata.obs else (
        "annotation_reason" if "annotation_reason" in adata.obs else None)
    if col is None:
        return None
    reason = adata.obs[col].astype(str)
    if "annotation_verdict" in adata.obs:
        reason = reason[adata.obs["annotation_verdict"].astype(str).to_numpy() == "FAIL"]
    blank = {"", "nan", "none", "na", "<na>", "assigned", "confident", "pass"}
    reason = reason[~reason.str.lower().isin(blank)]
    vc = reason.value_counts().head(10)
    if vc.empty:
        return None
    labels = [str(i).replace("_", " ") for i in vc.index][::-1]
